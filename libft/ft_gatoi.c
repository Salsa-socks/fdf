#include "libft.h"

int	ft_gatoi(char *str)
{
	size_t	i;
	size_t	sign;
	size_t	j;

	i = 0;
	j = 0;
	sign  = 1;
	if (str[j] == '-' || str[j] == '+')
	{
		sign = (str[j] == '-' ? - 1 : 1);
		j++;
	}
	while (str[j] != '\0' && str[j] >= '0' && str[j] <= '9')
	{
		i = i * 10 + str[j] - '0';
		j++;
	}
	return (i * sign);
}

